void clearScreen()
{
#ifdef _WIN32
  system("cls");
#else
  system("clear");
#endif
}

void handle_frames(const int key)
{
  if (key)
  {
    int i = 1;
    while (1)
    {
      if (i > 5)
      {
        i = 1;
      }
      clearScreen();
      return_frames(i);
      std::this_thread::sleep_for(std::chrono::milliseconds(100));
      i++;
    }
  }
}